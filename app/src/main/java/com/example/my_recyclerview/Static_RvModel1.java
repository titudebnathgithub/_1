package com.example.my_recyclerview;

public class Static_RvModel1 {


    private int image;
    private String text;
    private String text1;

    public Static_RvModel1(int image, String text, String text1) {
        this.image = image;
        this.text = text;
        this.text1 = text1;
    }

    public int getImage() {
        return image;
    }

    public String getText() {
        return text;
    }

    public String getText1() {
        return text1;
    }
}

